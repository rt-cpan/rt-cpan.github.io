#!/usr/bin/perl

use strict;
use warnings;

use Data::Dumper;

my @xlabels;
my @inoctets;
my @outoctets;
my $iface;

my $ymax = 0;

my $inoctets_min  = undef;
my $outoctets_min = undef;

my $old_inoctets  = undef;
my $old_outoctets = undef;

#open( FIFO, "< /tmp/eth0-stats.txt" );
while ( my $line = <DATA> ) {
    chomp($line);

    my ( $stattime, @data_tmp ) = split( ' ', $line );
    my $data = join( ' ', @data_tmp );
    $data =~ tr/ \t:/   /s;

    # face|bytes packets errs drop fifo frame compressed multicast|bytes packets errs drop fifo colls carrier compressed
    my ( %rx, %tx );
    (
        $iface,     $rx{bytes},   $rx{packets}, $rx{errs}, $rx{drop}, $rx{fifo},  $rx{frame},   $rx{compressed}, $rx{multicast},
        $tx{bytes}, $tx{packets}, $tx{errs},    $tx{drop}, $tx{fifo}, $tx{colls}, $tx{carrier}, $tx{compressed}
    ) = split( ' ', $data );

    if ( !defined($old_inoctets) and !defined($old_outoctets) ) {
        $old_inoctets  = $rx{bytes};
        $old_outoctets = $tx{bytes};
        next;
    }

    my $new_inoctets  = ( $rx{bytes} - $old_inoctets );
    my $new_outoctets = ( $tx{bytes} - $old_outoctets );

    $old_inoctets  = $rx{bytes};
    $old_outoctets = $tx{bytes};

    if ( $new_inoctets > $ymax )  { $ymax = ( $new_inoctets / 300 ) /  ( 2**20 ) }
    if ( $new_outoctets > $ymax ) { $ymax = ( $new_outoctets / 300 ) / ( 2**20 ) }

    if ( !defined($inoctets_min)  or ( $rx{bytes} < $inoctets_min ) )  { $inoctets_min  = $rx{bytes} }
    if ( !defined($outoctets_min) or ( $tx{bytes} < $outoctets_min ) ) { $outoctets_min = $tx{bytes} }

    push( @xlabels, ( $stattime - ( 5 * 3600 ) ) );
    push( @inoctets,  ( ( $new_inoctets / 300 ) /  ( 2**20 ) ) );
    push( @outoctets, ( ( $new_outoctets / 300 ) / ( 2**20 ) ) );
}
#close(FIFO);

use Chart::Gnuplot;

my $chart = Chart::Gnuplot->new(
    output    => "foo.png",
    title     => { text => "Device $iface Utilization", font => 'arial, 18', },
    imagesize => '1.7, 0.8',

    #    xlabel => { text => "Date/Time", },
    ylabel => { text     => "Throughput in Mbps", font => 'arial, 16' },
    xtics  => { labelfmt => '%b-%d\n%H:%M',       font => 'arial, 16' },
    ytics  => { labelfmt => '%.2f',               font => 'arial, 16' },
    grid   => "front",
    legend   => { position => "top right", width => 1, height => 1, border => "on", color => 'white' },
    plotbg   => { color    => "white" },
    bg       => { color    => "#EBEBCD" },
    timeaxis => 'x',
    xrange => [ $xlabels[0], $xlabels[$#xlabels] ],

    #    yrange => [ 0.0, ( $ymax  ) ],
    tmargin   => '3',
    lmargin   => '9',
    timestamp => { offset => '120, 28' },
);

$chart->label(
    text      => "Labeled at (-2, 0.5)",
    fontcolor => 'blue'
);

my $dataSet1 = Chart::Gnuplot::DataSet->new(
    xdata    => \@xlabels,
    ydata    => \@inoctets,
    title    => "Received",
    style    => "lines",
    linetype => 'solid',
    timefmt  => '%s',
    color    => "#FF8888",
    linetype => 'solid',
    width    => 5,
);

my $dataSet1a = Chart::Gnuplot::DataSet->new(
    xdata   => \@xlabels,
    ydata   => \@inoctets,
    timefmt => '%s',
    style   => "filledcurve x1",
    color   => "#FFDDDD",
);

my $dataSet2 = Chart::Gnuplot::DataSet->new(
    xdata    => \@xlabels,
    ydata    => \@outoctets,
    title    => "Transmitted",
    style    => "lines",
    timefmt  => '%s',
    color    => "#8888FF",
    linetype => 'solid',
    width    => 5,
);

$chart->plot2d( $dataSet1, $dataSet1a, $dataSet2 );

exit(0);

